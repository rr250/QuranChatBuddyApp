export const getLocalChapter = (surahNumber) => chapters[surahNumber] ?? null;

export const getLocalSurahIndex = () => surahIndex;

export const mapChapterToSurah = (chapter) => ({
    number: chapter.id,
    name: chapter.name,
    englishName: chapter.transliteration,
    englishNameTranslation: chapter.translation,
    numberOfAyahs: chapter.total_verses,
    revelationType: chapter.type === "meccan" ? "Meccan" : "Medinan",
    verses: chapter.verses.map((verse) => ({
        number: verse.id,
        numberInSurah: verse.id,
        text: verse.text,
        translation: verse.translation ?? "",
        transliteration: verse.transliteration ?? "",
    })),
});

export const mapIndexToSurahMeta = (surah) => ({
    number: surah.id,
    name: surah.name,
    englishName: surah.transliteration,
    englishNameTranslation: surah.translation ?? surah.transliteration,
    numberOfAyahs: surah.total_verses,
    revelationType: surah.type === "meccan" ? "Meccan" : "Medinan",
});
